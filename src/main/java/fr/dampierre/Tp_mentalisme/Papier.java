package fr.dampierre.Tp_mentalisme;

public class Papier {
    private int age;
    private int argent;

    Papier(int age, int argent){
        this.age = age;
        this.argent = argent;
    }
    public int getAge() {
        return age;
    }
    public int getArgent() {
        return argent;
    }
}
